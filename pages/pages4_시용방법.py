import streamlit as st

st.title("📘 사용 방법")

st.markdown("""
## 사용 방법

1. 유튜브에서 원하는 영상 링크를 복사합니다.
2. 앱에 링크를 붙여넣습니다.
3. 댓글 가져오기 버튼을 누릅니다.
4. 댓글을 표로 확인합니다.
5. 필요하면 CSV 파일로 다운로드합니다.

## 주의할 점

- 댓글이 꺼져 있는 영상은 댓글을 가져올 수 없습니다.
- 영상에 댓글이 너무 많으면 시간이 조금 걸릴 수 있습니다.
- 유튜브 정책이나 영상 설정에 따라 일부 댓글은 안 나올 수 있습니다.
""")
